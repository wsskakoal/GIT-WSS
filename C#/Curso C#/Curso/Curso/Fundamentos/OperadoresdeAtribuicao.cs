using System;
using System.Collections.Generic;
using System.Text;

namespace Curso.Fundamentos
{
    class OperadoresdeAtribuicao
    {
        public static void Executar()
        {
            var num1 = 3;
            num1 += 10; // num1 = num1 + 10;
            num1 -= 3; //num1 = num1 - 10;
            num1 *= 5; //num1 = num1 * 10;
            num1 /= 2; //num1 = num1 / 10;


            int a = 1;
            int b = a;

            a++; // a = a+1
            b--; // b = b-1

            // numero1++ 
        }
    }
